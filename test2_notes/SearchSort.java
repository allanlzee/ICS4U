package test2_notes;

public class SearchSort {
    /*
     * Linear Search 
     * 
     * Searching algorithm that locates an entry in a list of elements 
     * 
     * Effective/efficient for small lists, inefficient for large lists 
     * 
     * O(n) - linear time, worst case in time, best case in implementation 
     */

    /*
     * Binary Search 
     * 
     * Searching algorithm that locates an entry in an ordered list of elements 
     * 
     * Divide and conquer, cuts size of problem in half 
     * 
     * Number of comparsions = floor(log2(list size)) + 1 
     * 
     * Complexity: O(log(n))
     */
}
